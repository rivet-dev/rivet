// Generates `website/redirects.caddy` from the shared redirect map in
// `website/redirects.mjs`. The Caddyfile `import`s the emitted snippet so the
// production server returns real HTTP 301s (Astro's static redirect pages serve
// HTTP 200 with a meta-refresh, which search engines treat as soft redirects).
//
// One flat `redir` line is emitted per slash-variant of each source path. Flat
// lines (rather than a Caddy `map`) keep every rule individually auditable and
// let Caddy's case-insensitive path matchers normalize the request path.
//
// The output file is gitignored and regenerated during the Docker build.
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { redirects } from '../redirects.mjs';

// Conservative charset for paths. Blocks whitespace and any character that
// could break out of a `redir` argument or inject another Caddy directive.
const SAFE_PATH = /^\/[A-Za-z0-9\-._~/]*$/;

// Expands to `?query` when a query string is present, or an empty string when
// it is not, so redirects preserve query parameters (e.g. UTM tags).
const PREFIXED_QUERY = '{http.request.uri.prefixed_query}';

function assertSafe(path, kind) {
	if (typeof path !== 'string' || !SAFE_PATH.test(path)) {
		throw new Error(`unsafe ${kind} path in redirect map: ${JSON.stringify(path)}`);
	}
}

// Both the bare and trailing-slash forms of a source path, deduplicated. Caddy
// path matchers are exact (no trailing `*`), so each form needs its own rule.
function sourceVariants(from) {
	const bare = from.endsWith('/') ? from.slice(0, -1) : from;
	const variants = bare === '' ? ['/'] : [bare, `${bare}/`];
	return [...new Set(variants)];
}

const lines = [
	'# Generated by scripts/generate-caddy-redirects.mjs from redirects.mjs.',
	'# Do not edit by hand. This file is gitignored and rebuilt on every deploy.',
	'',
];

for (const [from, to] of Object.entries(redirects)) {
	assertSafe(from, 'source');
	assertSafe(to, 'target');
	for (const source of sourceVariants(from)) {
		lines.push(`redir ${source} ${to}${PREFIXED_QUERY} 301`);
	}
}

const outPath = fileURLToPath(new URL('../redirects.caddy', import.meta.url));
writeFileSync(outPath, `${lines.join('\n')}\n`);

console.log(`wrote ${outPath} (${Object.keys(redirects).length} redirects)`);
